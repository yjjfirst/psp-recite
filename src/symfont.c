static void symbol_draw_char(int x,int y,unsigned long color, char ch)
{
	unsigned char *cfont;
	unsigned char ch1;		//pointer to font
	unsigned long cx,cy;
	unsigned char b;
	
	cfont = symfont + ch * 16;
	unsigned long *addr = disp_get_vaddr(x, y);       
	for (cy = 0; cy < 16; cy++) {
		ch1 = *cfont;            
		b = 0x80;
		for (cx = 0; cx < 8; cx ++) {
			if ( (ch1 & b) == 0) {
				addr[cx + cy *512] = color;    
			}
			b = b >> 1;
		}
		cfont ++;
	}
}

void symbol_draw_string(int x,int y,unsigned long color, char* str)
{
	char* pr;
	int cx,cy, i;
	
	cx = x;
	cy = y;
	pr = str;
	i = 0;
	while( pr[0] != 0 ) {    		
    		symbol_draw_char(cx, cy, color, pr[0]);
    		cx += 8;
    		pr++;
		i++;
	}
}

